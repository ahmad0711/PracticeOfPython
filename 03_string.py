# string Concept
story = "once a boy name ahmad chaudhry.He want to learn python and he want to become a python full stack developer"
# Use for totle character of the story 
print(len(story))
print(story.endswith("developer"))
# use for checking words in story
print(story.count("a"))
# use for capitalize 
print(story.capitalize())
# To find a word
print(story.find("ahmad"))
# Using for replace word
print(story.replace("ahmad","zawar"))